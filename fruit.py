# Instance Variables


class Fruit:
    def __init__(self, name, color, flavour, size, texture):
        self.name = name
        self.color = color
        self.flavour = flavour
        self.size = size
        self.texture = texture


# OUTPUT
# >>> from fruit import Fruit
# >>> fruit1 = Fruit(name = "Apple", color = "red", flavour = "sweet", size = "small", texture = "smooth")
# >>> fruit1.flavour
# 'sweet'

